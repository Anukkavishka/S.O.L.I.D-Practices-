package Model;

public class UserStat {
    private String creditTotal;
    private String chargeTimeTotal;
    private String chargeTimeAvg;
    private String maxCredit;

    public String getCreditTotal() {
        return creditTotal;
    }

    public void setCreditTotal(String creditTotal) {
        this.creditTotal = creditTotal;
    }

    public String getChargeTimeTotal() {
        return chargeTimeTotal;
    }

    public void setChargeTimeTotal(String chargeTimeTotal) {
        this.chargeTimeTotal = chargeTimeTotal;
    }

    public String getChargeTimeAvg() {
        return chargeTimeAvg;
    }

    public void setChargeTimeAvg(String chargeTimeAvg) {
        this.chargeTimeAvg = chargeTimeAvg;
    }

    public String getMaxCredit() {
        return maxCredit;
    }

    public void setMaxCredit(String maxCredit) {
        this.maxCredit = maxCredit;
    }
}

